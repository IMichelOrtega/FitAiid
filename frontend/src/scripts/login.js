document.getElementById("loginForm").addEventListener("submit", async (e) => {
  e.preventDefault();

  const email = document.getElementById("email").value.trim();
  const password = document.getElementById("password").value.trim();

  if (!email || !password) {
    alert("Por favor ingresa tu correo y contrase√±a");
    return;
  }

  try {
    const response = await fetch("http://localhost:5000/api/auth/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ email, password })
    });

    const data = await response.json();

    if (response.ok) {
      alert("‚úÖ Inicio de sesi√≥n exitoso");
      console.log("üë§ Usuario:", data.user);
      console.log("üé´ Token:", data.token);

      // Puedes guardar el token si lo usas m√°s adelante
      localStorage.setItem("token", data.token);

      // Redirigir al dashboard o p√°gina principal
      window.location.href = "home.html";
    } else {
      alert(`‚ùå ${data.message || "Credenciales inv√°lidas"}`);
    }
  } catch (error) {
    console.error("‚ùå Error al conectar con el servidor:", error);
    alert("Error al conectar con el servidor");
  }
});
